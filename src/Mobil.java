    private void isiTabel(){
        //membuat display model table
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Kode Mobil");
        model.addColumn("Nama Mobil");
        model.addColumn("Tarif Sewa");
        
        try
        {
           String sql;
           sql="SELECT * FROM mobil";
           java.sql.Connection conn=(Connection)Config.configDB();
           java.sql.Statement stm=conn.createStatement();
           java.sql.ResultSet res=stm.executeQuery(sql);
           int no = 1;
           while(res.next())
           {
               model.addRow(new Object[]{no, res.getString(1), res.getString(2), res.getString(3)});
               no++;
           }
           tableMobil.setModel(model);
        }
        catch(Exception e)
        {
            
        }
    }

