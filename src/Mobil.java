    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if(txtKodeMobil.getText().equals("") || txtNamaMobil.equals("") || txtSewa.equals("")){
            JOptionPane.showMessageDialog(null, "Form masih kosong.");
        } else {
            try
            {
                String sql="INSERT INTO mobil VALUES('"+txtKodeMobil.getText() +"','"+txtNamaMobil.getText()+"', '"+ txtSewa.getText()+"')";
                java.sql.Connection conn=(Connection)Config.configDB();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Simpan Data Sukses");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            isiTabel();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String kodeMobil = txtKodeMobil.getText();
        
        if(kodeMobil.equals("")){
            JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu.");
        } else {
            String sql;
            try {
                sql="SELECT * FROM mobil WHERE kode_mobil = '"+kodeMobil+"'";
                java.sql.Connection conn=(Connection)Config.configDB();
                java.sql.Statement stm=conn.createStatement();
                java.sql.ResultSet res=stm.executeQuery(sql);
                int count  = 0;
                
                res.last();
                count = res.getRow();
                res.beforeFirst();

                if(count > 0){
                    sql="DELETE FROM mobil WHERE kode_mobil = '"+kodeMobil+"'";
                    java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Delete Data Sukses");
                } else {
                    JOptionPane.showMessageDialog(null, "ID tidak ditemukan.");
                }
                
            } catch (Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage()); 
            }
            isiTabel();
        }    
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String kodeMobil = txtKodeMobil.getText();
        String namaMobil = txtNamaMobil.getText();
        String sewa = txtSewa.getText();
        
        if(kodeMobil.equals("") || namaMobil.equals("") || sewa.equals("")){
            JOptionPane.showMessageDialog(null, "Pliih data terlebih dahulu.");
        } else {
            String sql;
            try {
                sql="SELECT * FROM mobil WHERE kode_mobil = '"+kodeMobil+"'";
                java.sql.Connection conn=(Connection)Config.configDB();
                java.sql.Statement stm=conn.createStatement();
                java.sql.ResultSet res=stm.executeQuery(sql);
                int count  = 0;
                
                res.last();
                count = res.getRow();
                res.beforeFirst();

                if(count > 0){
                    sql="UPDATE mobil SET nama_mobil = '"+ namaMobil +"', tarif = '"+ sewa + "' WHERE kode_mobil = '"+kodeMobil+"'";
                    java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Update Data Sukses");
                } else {
                    JOptionPane.showMessageDialog(null, "ID tidak ditemukan.");
                }
                
            } catch (Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage()); 
            }
            isiTabel();    
        }
    }//GEN-LAST:event_btnEditActionPerformed

